layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _QWERTY:
            set_led_off;
            break;
        case _SYMB:
            set_led_off;
            break;
        case _MOUSE:
            set_led_off;
            break;
        case _ARROW_R:
            set_led_off;
            break;
        case _ARROW_L:
            set_led_off;
            break;
        default:
            break;
    }
  return state;
}


